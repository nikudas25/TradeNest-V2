export function buildFallbackProduct(slug) {
  const product = fallbackProducts.find((item) => item.slug === slug);
  if (!product) {
    return null;
  }
  return {
    ...product,
    related_products: fallbackProducts.filter(
      (item) => item.slug !== slug && item.category_slug === product.category_slug,
    ).slice(0, 4),
  };
}
